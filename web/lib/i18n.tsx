export function translate(
  locale: Locale,
  key: TranslationKey,
  values: Record<string, string | number> = {},
): string {
  const template = locale === "es" ? spanish[key] : english[key];
  return template.replace(/{{(\w+)}}/g, (_, name: string) => String(values[name] ?? ""));
}

export function languageInstruction(locale: Locale): string {
  return locale === "es"
    ? "Write all user-facing narrative and questions in clear, plain Spanish. Preserve medication names, source quotations, identifiers, numeric values, and units exactly as provided."
    : "Write all user-facing narrative and questions in clear English. Preserve medication names, source quotations, identifiers, numeric values, and units exactly as provided.";
}

interface I18nValue {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey, values?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("en");

  useEffect(() => {
    // Hydrate the saved locale after mount so the server and first client
    // render both stay English, avoiding a hydration mismatch. The one-time
    // post-mount setState is intentional here.
    const saved = window.localStorage.getItem("sourced-locale");
    // eslint-disable-next-line react-hooks/set-state-in-effect -- deliberate post-hydration sync from localStorage
    if (saved === "en" || saved === "es") setLocaleState(saved);
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale;
  }, [locale]);

  const value = useMemo<I18nValue>(() => ({
    locale,
    setLocale: (next) => {
      setLocaleState(next);
      window.localStorage.setItem("sourced-locale", next);
    },
    t: (key, values) => translate(locale, key, values),
  }), [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("I18nProvider is missing");
  return value;
}
